// loops

// for of
// Recorre el objeto indicado despues de of, y deposita sus elementos en la variable declarada

let teachers = ['Alexys', 'Jon', 'Daniel', 'Fransisco', 'Rafa', 'Jose', 'Alvaro'];

for (let teacher of teachers) {
	console.log(teacher);
}

let nombre = 'Edward';

for (let letter of nombre) {
	console.log(letter);
}